def get_top_tags_selector():
    return ".tag-item a"  # Selector for top tags on the main page

def get_quotes_selector():
    return ".quote"  # Selector for individual quote blocks

def get_quote_text_selector():
    return ".text"  # Selector for quote text

def get_quote_author_selector():
    return ".author"  # Selector for quote author

def get_next_button_selector():
    return ".pager .next a"  # Selector for the "Next" button
