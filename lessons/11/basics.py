#real life example: építünk egy telken egy ikerházat

# program: ház építési terve, nem használ semmilyen erőforrást amíg el nem kezdődik az építkezés
# process: a ház tényleges építési folyamata, már erőforrásokat használva. ikerház miatt, lehet a két oldal 2 külön process
# thread: festés, falazás, elektronika, víz/gáz egy processen belül, tehát egy ház-oldal építésén belül